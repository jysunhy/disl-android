package ch.usi.dag.disl.exception;

public class StaticContextGenException extends DiSLException {

	private static final long serialVersionUID = 8195611687932799195L;

	public StaticContextGenException() {
		super();
	}

	public StaticContextGenException(String message, Throwable cause) {
		super(message, cause);
	}

	public StaticContextGenException(String message) {
		super(message);
	}

	public StaticContextGenException(Throwable cause) {
		super(cause);
	}
}
